# frozen_string_literal: true

# Calculator project to practice Ruby & RSpec
class Calculator
  def add(num1, num2)
    num1 + num2
  end

  def subtract(num1, num2)
    num1 - num2
  end

  def multiply(num1, num2)
    num1 * num2
  end

  def divide(num1, num2)
    num1 / num2
  end

  def x_to_the_y(num1, num2)
    num1**num2
  end

  def modulo(num1, num2)
    num1 % num2
  end
end
